let contador = 1 //declaração

while(contador <= 10){ //expressão (condição)
    console.log(`Contador : ${contador}`) 
    contador++ //incremento
}

for(let i = 1; i <= 10; i++){
    console.log(`i : ${i}`)
} //declaraçaõ; expressão; incremento

const nota = [5.6, 4, 10, 4.8, 2.4, 9.4]

for(let i = 0; i < nota.length; i++){
    console.log(`nota  : ${nota[i]}`)
}

